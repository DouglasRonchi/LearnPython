class Linguagens:
    __id = 'DEFAULT'
    __nome = ''
    __descricao = ''


    def get_id(self):
        return self.__id

    
    def set_id(self, id):
        self.__id = id


    def get_nome(self):
        return self.__nome


    def set_nome(self, nome):
        self.__nome = nome
    

    def get_descricao(self):
        return self.__descricao


    def set_descricao(self, descricao):
        self.__descricao = descricao
    